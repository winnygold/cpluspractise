// initiating a if loop using boolean variable

#include<iostream>
#include<string>

int main()
{
    // initiating a variable with boolean type
    bool flag = true;
   
    if (flag)
    {
        // since the variable hold boolean true , this loop will be printed
        std::cout << "Flag is true " << std::endl;
    }
    else
    {
        std::cout << "Flag is False" << std::endl;
    }
          
} 
